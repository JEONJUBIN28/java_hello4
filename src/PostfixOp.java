
public class PostfixOp {

	public static void main(String[] args) {
		
		//++ = num = num + 1;
		int num = 5;
		//System.out.println(++num);
		System.out.println((num++) + "");
		System.out.println((num++) + "");
		System.out.println(num + "\n");
		
		System.out.println((num--) + "");
		System.out.println((num--) + "");
		System.out.println(num);
		
		
	}

}


